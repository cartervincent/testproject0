def one():
    """
    一行代码打印乘法口诀
    :return:
    """
    print('\n'.join([' '.join(["%2s x%2s = %2s" % (j, i, i * j) for j in range(1, i + 1)]) for i in range(1, 10)]))


def two():
    """
    一行代码打印迷宫
    :return:
    """
